/// \file B1EventAction.cc
/// \brief Implementation of the B1EventAction class

#include "B1EventAction.hh"
#include "B1RunAction.hh"

#include "G4Event.hh"
#include "G4RunManager.hh"

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

B1EventAction::B1EventAction(B1RunAction* runAction)
: G4UserEventAction(),
  fRunAction(runAction),
  fEdep(0.)
{} 

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

B1EventAction::~B1EventAction()
{
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void B1EventAction::BeginOfEventAction(const G4Event*)
{    
  fEdep.clear();
  fpx.clear();
  fpy.clear();
  fpz.clear();
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void B1EventAction::EndOfEventAction(const G4Event*)
{   
  // accumulate statistics in run action
  G4double Esumtotal=0.;
  for(int i=0;i<fEdep.size();i++) {
    Esumtotal+=fEdep.at(i);
    fRunAction->AddEPoint(fEdep.at(i),G4ThreeVector(fpx.at(i),fpy.at(i),fpz.at(i)));
  }
  fRunAction->AddEdep(Esumtotal);
  fRunAction->Fill();
  Clear();
//  if(fEdep>0)G4cout<<"ProtonHit "<<fEdep<<G4endl;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void B1EventAction::AddEPoint(G4double Edep, G4ThreeVector position) {
  fEdep.push_back(Edep);
  G4double px=position.x();
  G4double py=position.y();
  G4double pz=position.z();
  fpx.push_back(px);
  fpy.push_back(py);
  fpz.push_back(pz);
}

void B1EventAction::Clear() {
  fEdep.clear();
  fpx.clear();
  fpy.clear();
  fpz.clear();
}
