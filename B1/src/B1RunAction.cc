void B1RunAction::BeginOfRunAction(const G4Run*)
{ 
  // inform the runManager to save random number seed
  G4RunManager::GetRunManager()->SetRandomNumberStore(false);
//  G4cout<<"FIRST"<<G4endl;
//  G4cout<<G4RunManager::GetRunManager()->ConfirmBeamOnCondition()<<G4endl<<G4endl<<G4endl<<G4endl;
//  for(int i=0;i<1e5;i++) G4cout<<i<<G4endl;
  G4cout<<"Run action "<<f<<G4endl;
  if(visual)f = new TFile("output.root","recreate");
  if(f!=0) {
    tree = new TTree("simData","BishSim data");
    tree->Branch("pE",&fpE);
    tree->Branch("px",&fpx);
    tree->Branch("py",&fpy);
    tree->Branch("pz",&fpz);
    G4cout<<"Writing to output.root"<<G4endl;

  }
  // reset accumulables to their initial values
  G4AccumulableManager* accumulableManager = G4AccumulableManager::Instance();
  accumulableManager->Reset();

}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void B1RunAction::EndOfRunAction(const G4Run* run)
{
  G4int nofEvents = run->GetNumberOfEvent();
  G4cout<<"Events "<<nofEvents<<G4endl;
  if (nofEvents == 0) return;
  if(f!=0) G4cout<<tree->GetEntries()<<G4endl;
  if(f!=0)tree->Write();
  if(f!=0)f->Close();
  return;
  // Merge accumulables 
  G4AccumulableManager* accumulableManager = G4AccumulableManager::Instance();
  accumulableManager->Merge();

  // Compute dose = total energy deposit in a run and its variance
  //
  G4double edep  = fEdep.GetValue();
  G4double edep2 = fEdep2.GetValue();
  
  G4double rms = edep2 - edep*edep/nofEvents;
  if (rms > 0.) rms = std::sqrt(rms); else rms = 0.;  

  const B1DetectorConstruction* detectorConstruction
   = static_cast<const B1DetectorConstruction*>
     (G4RunManager::GetRunManager()->GetUserDetectorConstruction());
  G4double mass = detectorConstruction->GetScoringVolume()->GetMass();
  G4double dose = edep/mass;
  G4double rmsDose = rms/mass;

  // Run conditions
  //  note: There is no primary generator action object for "master"
  //        run manager for multi-threaded mode.
  const B1PrimaryGeneratorAction* generatorAction
   = static_cast<const B1PrimaryGeneratorAction*>
     (G4RunManager::GetRunManager()->GetUserPrimaryGeneratorAction());
  G4String runCondition;
  if (generatorAction)
  {
    const G4ParticleGun* particleGun = generatorAction->GetParticleGun();
    runCondition += particleGun->GetParticleDefinition()->GetParticleName();
    runCondition += " of ";
    G4double particleEnergy = particleGun->GetParticleEnergy();
    runCondition += G4BestUnit(particleEnergy,"Energy");
  }
        
//
//

  // Print
  //  
  if (IsMaster()) {
    G4cout
     << G4endl
     << "--------------------End of Global Run-----------------------";
  }
  else {
    G4cout
     << G4endl
     << "--------------------End of Local Run------------------------";
  }
  return;
  G4cout
     << G4endl
     << " The run consists of " << nofEvents << " "<< runCondition
     << G4endl
     << " Cumulated dose per run, in scoring volume : " 
     << G4BestUnit(dose,"Dose") << " rms = " << G4BestUnit(rmsDose,"Dose")
     << G4endl
     << "------------------------------------------------------------"
     << G4endl
     << G4endl;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void B1RunAction::AddEdep(G4double edep)
{
  fEdep  += edep;
  fEdep2 += edep*edep;
}


//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void B1RunAction::AddEPoint(G4double edep, G4ThreeVector position) {
  fpE.push_back(edep);
  G4double px=position.x();
  G4double py=position.y();
  G4double pz=position.z();
  fpx.push_back(px);
  fpy.push_back(py);
  fpz.push_back(pz);
//  G4cout<<edep<<"\t"<<px<<"\t"<<py<<"\t"<<pz<<G4endl;

}

void B1RunAction::Fill() {
  if(f!=0)tree->Fill();
  fpE.clear();
  fpx.clear();
  fpy.clear();
  fpz.clear();
}
